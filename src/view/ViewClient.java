    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        mySQLcon.conectaBanco();
        novoCliente = new Cliente();
        String cpf = txtCPF2.getText();
        
        try {
            this.mySQLcon.executarSQL(
                   "SELECT "
                    + "nome,"                    
                    + "sexo,"
                    + "endereco,"
                    + "cidade,"
                    + "estado"
                 + " FROM"
                     + " cadastroclientes"
                 + " WHERE"
                     + " cpf = '" + cpf + "'"
                + ";"
            );
            
            while(mySQLcon.getResultSet().next()) {
                novoCliente.setNome(mySQLcon.getResultSet().getString(1));
                novoCliente.setSexo(mySQLcon.getResultSet().getString(2));
                novoCliente.setEndereco(mySQLcon.getResultSet().getString(3));
                novoCliente.setCidade(mySQLcon.getResultSet().getString(4));
                novoCliente.setEstado(mySQLcon.getResultSet().getString(5));
            }

            if (novoCliente.getNome().equals("")) {
                JOptionPane.showMessageDialog(null, "CPF não encontrado");
            } else {
                txtNome2.setText(novoCliente.getNome());
                txtEndereco2.setText(novoCliente.getEndereco());
                txtCidade2.setText(novoCliente.getCidade());
                
                cbSexo2.setSelectedItem(novoCliente.getSexo());
                cbEstado2.setSelectedItem(novoCliente.getEstado());
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Houve um erro na Busca.");
        } finally {
            mySQLcon.fechaBanco();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

