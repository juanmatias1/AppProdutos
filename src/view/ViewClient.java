    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        mySQLcon.conectaBanco();
        
        int status = 0;
        
        novoCliente = new Cliente();
        novoCliente.setNome(txtNome.getText());
        novoCliente.setCidade(txtCidade.getText());
        novoCliente.setCpf(txtCPF.getText());
        novoCliente.setEndereco(txtEndereco.getText());
        novoCliente.setEstado((String)cbEstado.getSelectedItem());
        novoCliente.setSexo((String)cbSexo.getSelectedItem());
        
        try {
            status = this.mySQLcon.insertSQL("INSERT INTO cadastroclientes ("
                    + "nome,"
                    + "cpf,"
                    + "sexo,"
                    + "endereco,"
                    + "cidade,"
                    + "estado"
                + ") VALUES ("
                    + "'" + novoCliente.getNome() + "',"
                    + "'" + novoCliente.getCpf() + "',"
                    + "'" + novoCliente.getSexo() + "',"
                    + "'" + novoCliente.getEndereco() + "',"
                    + "'" + novoCliente.getCidade() + "',"
                    + "'" + novoCliente.getEstado() + "'"
                + ");");
            
            if(status == 1) {
                JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso");
                apagaFormCadastro();
                
            }else {
                JOptionPane.showMessageDialog(null, "Houve um problema ao cadastrar Cliente.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Houve um erro no Cadastro.");
        }
        finally {
            mySQLcon.fechaBanco();
        }
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

