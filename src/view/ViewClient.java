    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        mySQLcon.conectaBanco();
        
        boolean status = false;
        
        String cpf = txtCPF2.getText();
        
        try {
            status = this.mySQLcon.updateSQL(
                "DELETE FROM cadastroclientes "
                + " WHERE "
                    + "cpf = '" + cpf + "'"
                + ";");
            
            if (status == true) {
                JOptionPane.showMessageDialog(null, "Cliente APAGADO com SUCESSO.");
            }else {
                JOptionPane.showMessageDialog(null, "Houve um problema ao APAGAR.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Houve um erro ao APAGAR o registro.");
        } finally {
            mySQLcon.fechaBanco();
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

